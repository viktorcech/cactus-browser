; ----------------------------------------------------------------------------
; net_open - Open FujiNet connection
; ----------------------------------------------------------------------------
.proc net_open
        jmp fn_open
.endp

; ----------------------------------------------------------------------------
; net_status - Get connection status
; Sets: zp_fn_bytes_lo/hi, zp_fn_connected, zp_fn_error
; ----------------------------------------------------------------------------
.proc net_status
        jmp fn_status
.endp

; ----------------------------------------------------------------------------
; net_read - Read data into rx_buffer
; Sets: zp_rx_len
; ----------------------------------------------------------------------------
.proc net_read
        jmp fn_read
.endp

; ----------------------------------------------------------------------------
; net_close - Close connection
; ----------------------------------------------------------------------------
.proc net_close
        jmp fn_close
.endp

; ----------------------------------------------------------------------------
; net_init - Initialize network (FujiNet, no selection needed)
; ----------------------------------------------------------------------------
.proc net_init
        rts
.endp

; ----------------------------------------------------------------------------
; net_shutdown - Cleanup on exit
; ----------------------------------------------------------------------------
.proc net_shutdown
        rts
.endp
